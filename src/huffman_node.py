class HuffmanNode:
    def __init__(self, character, frequency, left=None, right=None):
        self.__character = character
        self.__frequency = frequency
        self.__left = left
        self.__right = right

    def get_character():
        return self.__character
    
    def get_frequency():
        return self.__frequency

    def get_left_node():
        return self.__left
    
    def get_right_node():
        return self.__right
